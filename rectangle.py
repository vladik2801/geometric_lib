"""Файл работает с площадью и периметром прямоугольника"""
def area(a,b):
	'''Функция принимает на вход стороны прямоугольника a,b
	Вычисляет площадь и возвращает ее
	Пример использования:
	>>>area(10,5)
	output: 50
	'''
	return a * b
def perimeter(a, b):
	'''Функция принимает на вход стороны прямоугольника a,b
	Вычисляет периметр и возвращает его
	Пример использования: 
	>>>perimeter(10,5)
	output: 30
	'''
	return (a + b) * 2


